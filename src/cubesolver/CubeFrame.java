    private Color getColor(cubeColor c) {
        Color newColor;
        switch (c) {
            case WHITE:
                newColor = Color.WHITE;
                break;
            case RED:
                newColor = Color.RED;
                break;
            case BLUE:
                newColor = Color.BLUE;
                break;
            case ORANGE:
                newColor = Color.ORANGE;
                break;
            case GREEN:
                newColor = Color.GREEN;
                break;
            case YELLOW:
                newColor = Color.YELLOW;
                break;
            default:
                newColor = Color.WHITE;
                break;

        }
        return newColor;
    }
